def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser()
    parser.add_argument("roots", nargs="*", default=["src", "scripts", "tests"])
    return parser.parse_args()


# 本段代码核心功能：定义 `main`，用不生成缓存文件的方式完成TASK_024A Python语法检查。
# - 输入：参数为 `无显式参数`；路径和外部文本按UTF-8处理，安全开关必须由显式配置提供。
# - 处理：只执行函数名对应的单一职责；遇到缺字段、非法状态或越界值立即失败，不做静默猜测。
# - 输出：返回类型为 `int`；测试函数则通过断言表达通过或失败，不产生生产副作用。
# - 常量依据：任务号、状态枚举、零网络/零交易计数和候选优先级均来自TASK_022至TASK_024权威任务合同。
# - 为什么这样写：显式输入输出和失败模式便于教学排查，也让未来官方交易所或券商SDK只需通过薄适配器接入。

def main() -> int:
    args = parse_args()
    checked = 0
    # 本段代码核心功能：逐项遍历 `args.roots` 并处理 `root_name`。
    # - 输入：有限配置条目、发现证据或测试样本序列。
    # - 处理：每次循环只更新当前条目的局部结果，最终按稳定顺序汇总。
    # - 输出：更新局部结果、阻断原因或测试断言；不绕过上层来源和授权门禁。
    # - 常量依据：分支状态和零副作用要求来自对应TASK任务合同，不把经验猜测写成官方规则。
    # - 为什么这样写：逐项处理能保留来源级证据，避免聚合后无法追溯单个Provider。

    for root_name in args.roots:
        root = Path(root_name)
        # 本段代码核心功能：根据条件 `not root.exists()` 选择安全分支。
        # - 输入：条件表达式及此前已经规范化的局部变量。
        # - 处理：只执行满足合同的分支，非法状态通过异常或阻断原因显式返回。
        # - 输出：更新局部结果、阻断原因或测试断言；不绕过上层来源和授权门禁。
        # - 常量依据：分支状态和零副作用要求来自对应TASK任务合同，不把经验猜测写成官方规则。
        # - 为什么这样写：避免把缺失证据、未授权运行时或危险能力误判为可用。

        if not root.exists():
            continue
        # 本段代码核心功能：逐项遍历 `sorted(root.rglob('*.py'))` 并处理 `path`。
        # - 输入：有限配置条目、发现证据或测试样本序列。
        # - 处理：每次循环只更新当前条目的局部结果，最终按稳定顺序汇总。
        # - 输出：更新局部结果、阻断原因或测试断言；不绕过上层来源和授权门禁。
        # - 常量依据：分支状态和零副作用要求来自对应TASK任务合同，不把经验猜测写成官方规则。
        # - 为什么这样写：逐项处理能保留来源级证据，避免聚合后无法追溯单个Provider。

        for path in sorted(root.rglob("*.py")):
            source = path.read_text(encoding="utf-8-sig")
            compile(source, str(path), "exec")
            checked += 1
    # 本段代码核心功能：根据条件 `checked == 0` 选择安全分支。
    # - 输入：条件表达式及此前已经规范化的局部变量。
    # - 处理：只执行满足合同的分支，非法状态通过异常或阻断原因显式返回。
    # - 输出：更新局部结果、阻断原因或测试断言；不绕过上层来源和授权门禁。
    # - 常量依据：分支状态和零副作用要求来自对应TASK任务合同，不把经验猜测写成官方规则。
    # - 为什么这样写：避免把缺失证据、未授权运行时或危险能力误判为可用。

    if checked == 0:
        raise RuntimeError("no Python files were found for syntax checking")
    print(f"Python syntax files checked: {checked}")
    return 0


# 本段代码核心功能：根据条件 `__name__ == '__main__'` 选择安全分支。
